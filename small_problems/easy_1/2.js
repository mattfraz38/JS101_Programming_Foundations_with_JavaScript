for (let i = 1; i < 100; i += 2) {
  console.log(i);
}

/*
  A for loop begins with a local variable i is declared and assigned the value of 1. 
  i is logged to the console and our next iteration begins. On each iteration, i is 
  incremented by 2 and again logged to the console. This iteration continues until i 
  reachs 99 as the loop is set to terminate before reaching 100.
*/