/*
  Search Word (Part 2)

  QUESTIONS:
  INPUT:
    - two strings (a word and some text)
  OUTPUT:
    - one string (text)
  RULES:
    - given two arguments (a string word and some text) return the same text,
      however with the first argument words in the text highlighted (surrounded with ** and
      and the word itself in all uppercase chars)
    - Ex. '**SED** ut perspiciatis unde...'
  DATA STRUCTURES:
    - array and/or regex
  ALGORITHM:
    - using regex target the first argument words in the text
    - then replace the targeted word with **${target.toUpperCase()}**
*/

const searchWord = (target, text) => {
  const regex = new RegExp(`\\b${target}\\b`, 'gi');
  return text.replace(regex, `**${target.toUpperCase()}**`);
};

